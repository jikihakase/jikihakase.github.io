
;function suffix_for_size(img_size ) {
   if (img_size < 100) { return '_t'; }
   else if (img_size < 240) { return '_m'; }
   else if (img_size < 320) { return '_n'; }
   else if (img_size < 500) { return ''; }
   else if (img_size < 640) { return '_z'; }
   else if (img_size < 800) { return '_c'; }
   else { return '_b'; }
}


